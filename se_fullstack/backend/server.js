// server.js
require('dotenv').config();
const express = require('express');
const multer  = require('multer');
const { spawn } = require('child_process');
const path    = require('path');
const cors    = require('cors');
const { Pool } = require('pg');

// â”€â”€â”€ PICK A PORT â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Locally: always 5000.  In production: use the RENDERâ€supplied PORT env var.
const PORT =
  process.env.NODE_ENV === 'production'
    ? Number(process.env.PORT)
    : 5000;

// â”€â”€â”€ DATABASE URL CHECK â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const DATABASE_URL = process.env.DATABASE_URL;
if (!DATABASE_URL) {
  console.error('âŒ  DATABASE_URL environment variable is required');
  process.exit(1);
}

// â”€â”€â”€ POSTGRES POOL â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const pool = new Pool({
  connectionString: DATABASE_URL,
  ssl: { rejectUnauthorized: false }
});
pool.on('error', err => {
  console.error('ğŸ”´ Unexpected Postgres client error:', err);
  process.exit(-1);
});

// â”€â”€â”€ EXPRESS SETUP â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const app = express();
app.use(cors());
app.use(express.json());

// â”€â”€â”€ HEALTH CHECK â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.get('/health', (_, res) => res.send('OK'));

// â”€â”€â”€ MULTER UPLOAD SETUP â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const UPLOAD_DIR = path.join(__dirname, 'uploads');
const storage = multer.diskStorage({
  destination: UPLOAD_DIR,
  filename: (_, file, cb) => {
    cb(null, `upload_${Date.now()}${path.extname(file.originalname)}`);
  }
});
const upload = multer({ storage });

// â”€â”€â”€ PREDICTION ENDPOINT â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.post('/upload', upload.single('image'), (req, res) => {
  if (!req.file) {
    return res.status(400).json({ error: 'No file uploaded' });
  }
  const imgPath = req.file.path;

  // Call your YOLO script
  const py = spawn('python3', ['pred.py', imgPath]);
  let output = '';
  py.stdout.on('data', chunk => (output += chunk.toString()));
  py.stderr.on('data', chunk => console.error('ğŸ”´ Python error:', chunk.toString()));

  py.on('close', async code => {
    if (code !== 0) {
      return res.status(500).json({ error: `Prediction script exited with code ${code}` });
    }
    const result = output.replace(/^Detections:\s*/i, '').trim();

    // Store in Postgres
    try {
      const { rows } = await pool.query(
        `INSERT INTO predictions (image_path, result)
         VALUES ($1, $2)
         RETURNING id, image_path, result, timestamp`,
        [imgPath, result]
      );
      res.json(rows[0]);
    } catch (err) {
      console.error('ğŸ”´ DB insert error:', err);
      res.status(500).json({ error: 'Failed to save prediction' });
    }
  });
});

// â”€â”€â”€ SERVE REACT IN PRODUCTION â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
if (process.env.NODE_ENV === 'production') {
  const clientBuild = path.resolve(__dirname, '../frontend/build');
  app.use(express.static(clientBuild));
  app.get('*', (_, res) => {
    res.sendFile(path.join(clientBuild, 'index.html'));
  });
}

// â”€â”€â”€ START SERVER â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.listen(PORT, () => {
  console.log(`ğŸš€ Server listening on port ${PORT}`);
});
